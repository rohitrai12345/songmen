# Like in any other language python also has a set of datatypes
# There are two types of datatypes:
    # 1. Mutable
    # 2. Immutable

# Those objects or datatypes which can be changed even after their creation, such datatypes are called mutable
# Unlike mutable objects, immutable objects can't be changes once they are created


# Numbers (int, float, complex), tuples, strings, boolean are the immutable datatypes in python
# List, Set, Dictionary are the mutable datatypes in python
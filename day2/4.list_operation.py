# Addition (+)

a = [1, 2, 3]
b = [4, 5, 6]
print(a + b)  #  [1, 2, 3, 4, 5, 6]


# Repetition Operation (*)

a = [1, 2, 3]
print(a * 3)  # [1, 2, 3, 1, 2, 3, 1, 2, 3]


# Membership Check
# "in" and "not in" are the membership operators

print("a" in ["a", "e", "i"])  # True
print(2 not in [1, 2, 3])  # False